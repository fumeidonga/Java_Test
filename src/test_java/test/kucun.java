package test_java.test;

public class kucun {

	// 规格代码
	public String code;
	// 可配数
	public String num;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	@Override
	public String toString() {
		return "kucun [code=" + code + ", num=" + num + "]";
	}

}
